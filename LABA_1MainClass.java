/**
 * @file LABA_1MainClass.java
 * @brief Главный класс для нахождения минимального и максимального значений в серии целых чисел.
 *
 * Эта программа запрашивает у пользователя ввод серии целых чисел, 
 * затем вычисляет и выводит минимальные и максимальные значения среди введённых чисел.
 * В конце программа отображает имя автора.
 */

import java.util.Scanner;

/**
 * @class LABA_1MainClass
 * @brief Главный класс программы.
 *
 * Этот класс содержит главный метод, который является точкой входа в программу.
 */
public class LABA_1MainClass {

    /**
     * @brief Главный метод для выполнения программы.
     *
     * Этот метод запрашивает у пользователя количество чисел, которые он хочет ввести,
     * считывает эти числа, определяет среди них минимальные и максимальные значения.
     * В конце метод выводит минимальные и максимальные значения и отображает имя автора.
     *
     * Основные действия метода включают:
     * - Инициализацию сканера для считывания ввода пользователя.
     * - Запрос у пользователя количества чисел для ввода.
     * - Считывание чисел и нахождение минимального и максимального значений.
     * - Вывод минимального и максимального значений.
     * - Закрытие сканера.
     * - Вывод имени автора.
     *
     * @param args Аргументы командной строки (не используются).
     */
    public static void main(String[] args) {
        // Инициализация сканера для считывания ввода пользователя
        Scanner scanner = new Scanner(System.in);

        // Запрос у пользователя количества чисел для ввода
        System.out.print("Введите количество чисел: ");
        int n = scanner.nextInt();

        int min = Integer.MAX_VALUE; ///< Переменная для хранения минимального значения
        int max = Integer.MIN_VALUE; ///< Переменная для хранения максимального значения

        // Запрос ввода чисел у пользователя
        System.out.println("Введите " + n + " целых чисел:");

        /**
         * @brief Цикл для считывания чисел и нахождения минимального и максимального значений.
         *
         * Этот цикл выполняется n раз, считывая каждое введённое число и обновляя значения
         * минимального и максимального чисел при необходимости.
         *
         * @param i Индекс текущей итерации цикла.
         * @param number Текущее введённое число.
         */
        for (int i = 0; i < n; i++) {
            int number = scanner.nextInt();
            
            // Проверка и обновление минимального значения
            if (number < min) {
                min = number;
            }
            
            // Проверка и обновление максимального значения
            if (number > max) {
                max = number;
            }
        }

        // Вывод минимального значения
        System.out.println("Наименьшее число: " + min);
        
        // Вывод максимального значения
        System.out.println("Наибольшее число: " + max);

        // Закрытие сканера для освобождения ресурсов
        scanner.close();

        // Вывод имени автора
        System.out.println("\nГузанов Иван Вадимович");
    }
}